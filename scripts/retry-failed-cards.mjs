// One-off: regenerate the 3 cards that gpt-image-1 refused via content
// policy in the main sweep. Each gets a softened subject prompt that
// avoids the language the filter caught while preserving the card's
// intent.
//
// Run:
//   OPENAI_API_KEY=sk-... node scripts/retry-failed-cards.mjs

import fs from "node:fs/promises";
import path from "node:path";
import sharp from "sharp";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.join(__dirname, "..");
const OUT_DIR = path.join(__dirname, "generated-card-art");
const PUBLIC_DIR = path.join(REPO_ROOT, "public", "cartoon-banners");
const MANIFEST_PATH = path.join(REPO_ROOT, "src", "app", "data", "cartoon-manifest.ts");
const GEN_SIZE = "1536x1024";
const QUALITY = "medium";

const API_KEY = process.env.OPENAI_API_KEY;
if (!API_KEY) { console.error("Missing OPENAI_API_KEY"); process.exit(1); }

const STYLE_PROMPT =
  "Create a clean modern comic-book illustration adapted for a wide horizontal banner. " +
  "Clean black ink linework (refined, not heavy), flat colors with light gradient shading, subtle Ben-Day dot accents used sparingly only on skin tones or sky. " +
  "Palette: cream/off-white background, warm orange (#ed6624), deep navy (#23297e), rich purple (#5a3e9e), sky blue, occasional muted teal. Optimistic, hopeful, vibrant — not dark or overwhelming. " +
  "COMPOSITION — wide horizontal banner. Place the main subject (any face or head) in the UPPER portion of the frame. Heads near the top. Lower body, props, or ground can fill the bottom. " +
  "TEXT RULES — short real-word slogans on signs are FINE (STOP, NO ICE, RESIST, VOTE NO). " +
  "NEVER invent fake/nonsense words or paint the card title as text in the image.";

// Three holdouts with softened subject prompts that avoid the content
// filter triggers from the original prompts.
const CARDS = [
  {
    id: 1174,
    subject: "An artist in a sunny studio sketching colorful protest posters on a wide drafting table. Stack of finished posters with bold designs. Walls covered in inspiration board pieces. Warm, hopeful printmaker workshop vibe. Person looking down at their work, focused.",
  },
  {
    id: 1336,
    subject: "A lit handmade resistance soy candle on a wooden side table, soft warm glow, colorful printed wrap-around label with a bold political slogan, cozy domestic scene with a bookshelf and plants in the background. The candle is the focal point, centered.",
  },
  {
    id: 2171,
    subject: "A cheerful young person holding up a smartphone showing a social media app, with a stylized butterfly icon and a Tesla protest organizing group's profile visible on the screen. Tesla building blurred in the background. Sky-blue background.",
  },
];

async function generateOne(card) {
  console.log(`\n[${card.id}] retry with softened prompt`);
  const prompt = `${STYLE_PROMPT}\n\nSubject: ${card.subject}`;
  const r = await fetch("https://api.openai.com/v1/images/generations", {
    method: "POST",
    headers: { "Content-Type": "application/json", Authorization: `Bearer ${API_KEY}` },
    body: JSON.stringify({ model: "gpt-image-1", prompt, size: GEN_SIZE, quality: QUALITY, n: 1 }),
  });
  if (!r.ok) throw new Error(`OpenAI ${r.status}: ${(await r.text()).slice(0, 400)}`);
  const json = await r.json();
  const b64 = json?.data?.[0]?.b64_json;
  if (!b64) throw new Error("No image data");
  const buf = Buffer.from(b64, "base64");
  const webp = await sharp(buf).webp({ quality: 80 }).toBuffer();
  await fs.writeFile(path.join(OUT_DIR, `card-${card.id}.webp`), webp);
  await fs.writeFile(path.join(PUBLIC_DIR, `card-${card.id}.webp`), webp);
  console.log(`  ✓ saved ${webp.length / 1024 | 0} KB`);
}

async function writeManifest() {
  const files = await fs.readdir(PUBLIC_DIR);
  const ids = files
    .filter((f) => /^card-\d+\.webp$/.test(f))
    .map((f) => Number(f.match(/^card-(\d+)\.webp$/)[1]))
    .sort((a, b) => a - b);
  const body = `/**
 * Manifest of card IDs that have a cartoonized banner in
 * \`public/cartoon-banners/card-{id}.webp\`.
 *
 * Auto-generated by \`scripts/generate-card-art.mjs\`. Hand edits will be
 * overwritten on the next generation run — if you want to remove a card
 * from cartoonization, also delete its webp from
 * \`public/cartoon-banners/\` so this list stays in sync.
 *
 * resolveCard() in App.tsx checks membership of this Set; matching cards
 * get \`cartoonImageUrl = "/cartoon-banners/card-\${id}.webp"\` set, which
 * ActionCard already prefers over the original topImage.
 */
export const CARTOON_IDS: ReadonlySet<number> = new Set([
  ${ids.join(", ")},
]);

/** Returns the public URL for a cartoonized banner, or null if the card
 *  hasn't been cartoonized yet. */
export function cartoonUrlFor(cardId: number | undefined | null): string | null {
  if (typeof cardId !== "number") return null;
  if (!CARTOON_IDS.has(cardId)) return null;
  return \`/cartoon-banners/card-\${cardId}.webp\`;
}
`;
  await fs.writeFile(MANIFEST_PATH, body);
}

let ok = 0, fail = 0;
for (const c of CARDS) {
  try { await generateOne(c); ok++; await writeManifest(); }
  catch (e) { console.error(`  ✗ ${c.id}: ${e.message}`); fail++; }
}
console.log(`\nDone. ${ok} succeeded, ${fail} failed.`);
